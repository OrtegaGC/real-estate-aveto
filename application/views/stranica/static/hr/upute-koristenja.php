	<br>
	<p>
		<strong class="istaknuto boja"> CROATIA-AVETO: </strong>
	</p>
	<p>
		Riječ <i>aveto</i> – latinskog je porijekla i u skraćenom obliku znači <i>dobrodošao</i>
	</p>
	<p>
		<span class="istaknuto boja"> Croatia Aveto</span> - web-portal namijenjen vlasnicima smještajnih ugostiteljskih objekata (hotela, turističkih apartmana, pansiona kampova, gostionica) zainteresiranih za oglašavanje na internetu..
	</p>
	<p>
		Ukoliko ste i sami vlasnikom takvog objekta i želite predati oglas, registrirajte se na web-stranici našeg portala (<a href="http://croatia-aveto.com.hr">www.croatia-aveto.com.hr</a>).
	</p>

	<p>
		Prednosti oglašavanja na Croatia - Aveto stranici:
	</p>
	<ul>
		<li>
			oglašavanje na internetu 24 sata dnevno, 365 dana u godini
		</li>
		<li>
			pregled ponude i potražnje
		</li>
		<li>
			izravan kontakt s gostima
		</li>
		<li>
			pogodnost samostalnog uređivanja oglasa u svakom trenutku
		</li>
		<li>
			mogućnost popunjavanja smještajnih jedinica i izvan glavne sezone (u predsezoni i posezoni)
		</li>
		<li>
			mogućnost iznajmljivanja Vaših smještajnih jedinica putem mreže naših partnera izvan Hrvatske
		</li>
		<li>
			besplatan programski paket namijenjen iznajmljivačima i usklađen sa zakonskom regulativom RH
		</li>
		<li>
			vidljiv broj posjećenosti (uz svaki oglas)
		</li>
		<li>
			provizija se ne naplaćuje
		</li>
	</ul>

	<p>
		Samo je jedan nedostatak oglašavanja na našem portalu: cijena godišnjeg oglašavanja iznosi 100 kuna.
	</p>
	<p>
		<strong class="istaknuto boja"> <span class="istaknuto boja"> REGISTRACIJA </span> </strong>
	</p>
	<p>
		Prilikom registracije prvo birate kategoriju <i>ponude</i> ili <i>potražnje</i>, ovisno o tome oglašavate li svoj turistički objekt ili potražujete smještaj za odmor i putovanje.
	</p>
	<ul>
		<li>
			Obvezan unos podataka označenih zvjezdicom.
		</li>
		<li>
			Podaci koji nisu označeni zvjezdicom i čiji unos nije obvezan mogu se ispuniti i naknadno.
		</li>
		<li>
			Kliknite na gumb <strong>Registracija</strong>
		</li>
		<li>
			Prilikom registracije prihvaćate uvjete korištenja naših stranica koje možete pročitati <strong> <a href="http://croatia-aveto.com.hr/stranica/uvjeti_koristenja">OVDJE</a> </strong>
		</li>
		<li>
			Zapamtite svoje korisničko ime i lozinku. To su ključni podaci nužni za ponovno prijavljivanje na na stranicu <a href="http://croatia-aveto.com.hr">www.croatia-aveto.com.hr</a>.
		</li>
	</ul>

	<p>
		<strong class="istaknuto boja"> <span class="istaknuto boja"> UNOS OGLASA - PONUDA (oglašavanje turističkog smještaja) </span> </strong>
	</p>
	<ul>
		<li>
			Registracijom stječete mogućnost unosa oglasa Vašeg turističkog objekta.
		</li>
		<li>
			Osim unosa osnovnih podataka imate mogućunost označiti Vaš smještaj na <em><i>Google karti</i></em>. To činite na sljedeći način:
		</li>
		<li>
			Na samoj karti mjesto ćete označiti pomoću crvenog pokazivača koji ćete postaviti na onom mjestu na karti koje odgovara lokaciji Vašeg smještaja.
		</li>

	</ul>

	<p>
		Daljnji unos oglasa:
	</p>
	<ul>
		<li>
			Obvezan unos polja označenih zvjezdicom.
		</li>
		<li>
			Podatke čiji unos nije obvezan možete upisati naknadno, no potrudite se unijeti što više podataka kako biste oglas učinili informativnijim i preciznijim.
		</li>
		<li>
			Dodavanje više smještajnih ugostiteljskih objektata u jednom oglasu (više apartmana, studio-apartmana, soba i sl.)
		</li>
		<li>
			Nakon što ste ispunili svoj oglas, objavite ga pritiskom na gumb <strong>Spremi upis.</strong>
		</li>
	</ul>

	<p>
		<strong class="istaknuto boja"> <span class="istaknuto boja"> UNOS OGLASA - POTRAŽNJA (traženje turističkog smještaja) </span> </strong>
	</p>
	<ul>
		<li>
			Registracijom stječete mogućnost unosa oglasa za Vašu potražnju turističkog objekta.
		</li>
		<li>
			Izaberite destinaciju u kojoj želite iznajmiti smještajni ugostiteljski objekt i ondje provesti svoj godišnji odmor.
		</li>
		<li>
			Obvezan unos polja označenih zvjezdicom.
		</li>
		<li>
			Podatke čiji unos nije obvezan niste dužni ispunjavati, no potrudite se unijeti što više podataka (broj ležajeva, broj spavaćih soba, balkon, pogled na more, udaljenost od centra grada i najbliže trgovine i sl.) kako biste oglas učinili informativnim i s jasnim opisom Vaših afiniteta.
			Na taj način činite uslugu i sebi i iznajmljivačima budući da će im tako ispunjen oglas olakšati potražnju i stupanje u kontakt s Vama. Drugim riječima, detaljnijim i obuhvatnijim oglasom izbjeći ćete  tapkanje u mraku, a Vi i iznajmljivač lakše ćete se i brže naći na – pola puta.
		</li>
		<li>
			Nakon što ste ispunili svoj oglas objavite ga pritiskom na gumb <strong>Spremi upis.</strong>
		</li>
	</ul>

