		<h3 class="istaknuto boja-3 velicina">Uvjeti korištenja / oglašavanja</h3>
	</p>
	<br>
	<p>
		&nbsp;
	</p>
	<p>
		1. Web-stranica croatia-aveto.com.hr u vlasništvu je društva ScandiEuro d.o.o. Pristupanjem web-stranicama portala Croatia Aveto prihvaćate sva pravila i uvjete korištenja ovih stranica. Zadržavamo pravo promjene i modifikacije uvjeta korištenja ovih stranica u bilo kojem trenutku. Promjene stupaju na snagu u trenutku objave. Korisnik je dužan redovito čitati pravila i uvjete korištenja te se smatra da je pristupanjem stranicama Croatia Aveto upoznat s aktualnim pravilima i uvjetima njihova korištenja.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		2. Oglašivač je ujedno i sam vlasnik (-ica) oglašenog smještajnog objekta, koji mora posjedovati svu potrebnu dokumentaciju za iznajmljivanje.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		3. U svrhu oglašavanja na portalu Croatia Aveto, korisnik se obvezuje registrirati i u registracijskom obrascu popuniti svoje osobne podatke i podatke za kontakt. Prikupljanje traženih podataka utemeljeno je Zakonom o zabrani i sprečavanju obavljanja neregistrirane djelatnosti. Korisnik točnost podataka jamči zakonskom odgovornošću. Sukladno Zakonu o zaštiti osobnih podataka prikupljeni osobni podaci pohranjuju se u zbirci osobnih podataka.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		4. Svaki korisnik prihvaćanjem ovih pravila dopušta uporabu i obradu navedenih podataka u svrhu djelovanja sustava, ali i za slanje elektronske pošte u skladu s dolje navedenim, a ScandiEuro istodobno se obvezuje da će te podatke koristiti isključivo u svrhu kojoj su i namijenjeni.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		5. Sve objave, oglasi, poruke, tekstovi, prikazi, fotografije, video snimke i drugi materijali (u daljnjem tekstu: sadržaj) objavljeni na stranicama, preneseni putem stranica ili linkani sa stranica, isključiva su odgovornost osobe od koje je takav sadržaj i potekao. ScandiEuro d.o.o ne daje nikakva jamstva glede točnosti, potpunosti ili autentičnosti takvog sadržaja.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		6. U slučaju bilo kakvih zahtjeva trećih osoba na osnovi naknade štete prema izdavaču internet portala njuskalo.hr, a po bilo kojoj osnovi u vezi s dostavljenim sadržajem, korisnik preuzima odgovornost,kao i obvezu, poduzeti sve radnje kako bi obranio i zaštitio izdavača ScandiEuro d.o.o. od svih takvih zahtjeva. Korisnik se obvezuje nadoknaditi ScandiEuru d.o.o. svaku štetu nastalu temeljem zahtjeva trećih osoba, po bilo kojoj osnovi u vezi s dostavljenim materijalom.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		7. Zabranjeno je naručivanje i objavljivanje sadržaja koji slovi kao nezakonit, štetan, prijeteći, koji zlostavlja, uznemiruje, kleveće ili na bilo koji način šteti malodobnim osobama. Nedopušteni su sadržaji koji sadrže osobne podatke drugih osoba bez njihova izričitog odobrenja. Korisnik se obvezuje da neće objavljivati oglase u suprotnosti sa zakonom, ostalim propisima i Ustavom Republike Hrvatske. Korisnik se posebno obvezuje da neće prodavati i oglašavati pružanje usluga kojima bi se kršila autorska i srodna prava trećih osoba.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		8. Svi eventualni sporovi nastali iz odnosa korisnika i oglašivača (primjerice, zbog istinitosti podataka o korisniku ili oglašivaču, sadržaju ponude, o predmetu i cijeni ponude i sl.) rješavaju se isključivo između korisnika i oglašivača. Croatia Aveto nije odgovoran za bilo kakvu štetu bilo koje vrste nastale kao posljedicu takvih odnosa.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		9. Slike u oglasu moraju biti slike objekata koji se iznajmljuju.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		10. Oglasi moraju biti napisani na hrvatskom jeziku.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		11. Internetske veze (linkovi) na web-stranicu croatia-aveto.com.hr: dopušteno je isključivo povezivanje na dodatni opis predmeta. Linkovi na stranice koje oglašavaju razne usluge nisu dopuštene. Veze na stranice koje su konkurentske Croatia Avetu također nisu dopuštene. Croatia Aveto zadržava pravo procijene hoće li neki link biti odobren ili ne.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		12. Objavom oglasa na Croatia Avetu korisnik se obvezuje da će savjesno odgovarati na upite ostalih korisnika ( telefonom ili e-mailom). Korisnik mora imati aktivnu e-mail adresu na kojoj povremeno pregledava poštu.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		13. Korisnik je dužan brinuti o sigurnosti svoje korisničke lozinke i povremeno ju mijenjati. Croatia Aveto ne odgovara za slučajeve zlouporabe korisničke lozinke. Međutim, nakon što korisnik dojavi potencijalnu zloupotrebu odgovarajuće će postupiti. Korisnik se slaže s povremenim primanjem elektronske pošte povezane s aktivnostima Croatia Aveto. Elektronska pošta može povremeno sadržavati reklamne poruke. Korisnik u bilo koje vrijeme može pismeno zahtijevati da Croatia Aveto trajno ili privremeno prestane koristiti njegove osobne podatke za slanje poruka.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		14. Nije dopušteno kopiranje i uporaba bilo kojeg dijela stranice Croatia Aveto uključujući i podatke koje daju korisnici, bez prethodnog pismenog dopuštenja ScandiEuro d.o.o.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		15. Croatia Aveto zadržava pravo s popisa korisnika trenutačno brisati one korisnike koji bi kršili ova pravila i uvjete ili na neki drugi način usporavali ili ometali rad stranica. ScandiEuro d.o.o. zadržava pravo poduzeti odgovarajuće mjere protiv takvih korisnika. Uvjeti i odnosi između korisnika i društva ScandiEuro d.o.o. podliježu zakonima Republike Hrvatske. U slučaju spora nadležan je stvarno nadležni sud u Zagrebu.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		16. ScandiEuro d.o.o. ne odgovara za bilo kakvu štetu koja bi mogla nastati zbog prekida dostupnosti, neobjavljivanja, odnosno brisanja oglasa ili grešaka u djelovanju web-stranica.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		17. Croatia Aveto ne jamči da će korisnik moći pristupati do sadržaja svog oglasa kada on jednom istekne. Stoga se preporučuje da korisnik pohrani sadržaj i slike oglasa na svome računalu za slučaj da će ih kasnije htjeti upotrijebiti u nekom drugom kontekstu.
	</p>
	<p>
		&nbsp;
	</p>
	<p>
		18. Croatia Aveto zadržava pravo brisanja, odnosno neobjavljivanja oglasa koji ne zadovoljavaju određeni kriterij. Croatia Aveto ima pravo urediti, izmijeniti ili ne objaviti oglase, i to bez najave, a to će se dogoditi u sljedećim okolnostima:
	</p>
	<p>
		&nbsp;
	</p>
	<ul>
		<li>
			kada je oglas predan u krivu rubriku,
		</li>
		<li>
			kada proizvod i/ili oglašivač krše autorska prava i/ili druga prava intelektualnog vlasništva,
		</li>
		<li>
			kada proizvod može uzrokovati štetu i/ili prekršiti prava intelektualnog vlasništva nekom drugom na bilo koji način,
		</li>
		<li>
			kada proizvod i/ili sadržaj oglasa sadrži diskriminirajuće aspekte, (dječje) pornografske materijale ili, sukladno hrvatskom zakonu, ilegalne materijale,
		</li>
		<li>
			kada sadržaj jednog oglasa sadrži više proizvoda ili usluga koji nisu raspoređeni u ispravne kategorije,
		</li>
		<li>
			kada je oglas u bilo kojem smislu uvredljiv,
		</li>
		<li>
			kada je oglas sumnjiv u smislu oglašavanja ukradene ili ilegalne robe,
		</li>
		<li>
			kada oglas na bilo koji način narušava kvalitetu sadržaja.
		</li>
	</ul>
